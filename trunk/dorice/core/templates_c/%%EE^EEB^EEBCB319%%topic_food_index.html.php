    //瀑布流布局
    var marLeft=23;
    var marTop=25;
    var marBottom=20;
    var li=$(".food_one");
    var li_w=li[0].offsetWidth;
    function sort(){
        var h=[];
        var n=$("#food_show").width()/li_w|0;
        for(var i=0;i<li.length;i++){
            var li_h=li[i].offsetHeight;
            if(i<n){
                h[i]=li_h;
                li.eq(i).css("top",0);
                li.eq(i).css("left",i * (li_w+marLeft));
            }else{
                min_H =Math.min.apply(null,h);
                minKey = getarraykey(h, min_H);//
                h[minKey] += li_h+marTop;
                li.eq(i).css("top",min_H+marTop);//
                li.eq(i).css("left",minKey * (li_w+marLeft));    
            }
        }
        max =Math.max.apply(null,h)+marBottom ;
        $("#food_show").css("height",max); 
    }
    function getarraykey(s, v) {for(k in s) {if(s[k] == v) {return k;}}}
    //window.onload = window.onresize = function() {sort()};
    sort();
</script>
